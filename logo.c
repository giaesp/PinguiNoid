void DrawObject(int x, int y, int xlimit, int ylimit,
                int object[][xlimit], int mode)
{
  int i, j, color;
  for(j=0;j<ylimit;j++) {
    for(i=0;i<xlimit;i++) {
      if(mode == OMBRA) vga_setcolor(0);
      else if(mode == SOLID) vga_setcolor(object[j][i]);
      vga_drawpixel(x+i, y+j);
    }
  }
}




